package Lec2;

import java.util.Scanner;

public class print_hello_5_times{
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int i = 1;
        while(i<=n){
            System.out.println("Game of thrones , One punch Man");
            i+=1;
        }
    }
}
